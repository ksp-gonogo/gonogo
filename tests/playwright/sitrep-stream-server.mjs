#!/usr/bin/env node
/**
 * Fake Sitrep mod WebSocket server for multi-screen Playwright tests.
 * Replays a fixed, hand-authored telemetry snapshot to any client that
 * subscribes, so a real `SitrepTelemetryProvider`/`WebSocketTransport` in a
 * real Chromium tab can drive its dashboard widgets without needing KSP or
 * the mod running.
 *
 * Successor to `telemachus-replay-server.mjs`, retired once the app's only
 * telemetry source became the Sitrep stream (`806e7fe2`, "delete the legacy
 * Telemachus data source"). Same snapshot-replay model, new wire protocol:
 *
 *   Client -> server: `{"type":"subscribe","topic":"<t>"}` /
 *                      `{"type":"unsubscribe","topic":"<t>"}`
 *   Server -> client, once per subscribe: a `stream-data` frame carrying the
 *   topic's current snapshot value (if this fixture has one), then the same
 *   frame again every 250ms for as long as the topic stays subscribed — this
 *   mirrors the real mod's change-gated-but-periodically-reconfirmed delivery
 *   and covers the same "late subscriber" race the Telemachus replay server's
 *   own doc comment described (WebSocketTransport re-subscribes every still-
 *   active topic on reconnect; a snapshot re-emit means a late/reconnected
 *   subscriber never waits longer than one tick).
 *
 * SNAPSHOT SCOPE — deliberately not "every Sitrep topic". Real recorded wire
 * fixtures exist (`local_docs/telemetry-mod/recordings/*.json`, generated by
 * `mod/Sitrep.Host.IntegrationTests/WireFixtureGeneratorTests.cs`) but are
 * gitignored/local-only and don't cover every topic anyway — not usable as a
 * committed CI fixture. This snapshot is hand-authored instead, translating
 * the OLD `test/recorded_fixtures/launch_to_apoapsis_10000.json` Telemachus
 * flight (a Kerbin-orbiting "Mun Tester" craft, Bob Kerman aboard, LOS at
 * end of recording) into the new topic/field shapes via
 * `packages/sitrep-client/src/map-topic.ts`'s migration table — chosen so
 * every widget spec that asserted a specific formatted value against the old
 * fixture (e.g. SemiMajorAxis's "773.9 km", CommSignal's "LOS") sees the
 * IDENTICAL underlying number/string here and needs no assertion rewrite.
 *
 * Topics with no snapshot entry below are simply never sent — exactly the
 * "missing key" behaviour the old Telemachus replay server had for keys
 * absent from its recording (confirmed safe: an un-carried-or-never-arrived
 * topic resolves to `undefined` forever, no throw — see
 * `packages/core/src/hooks/useTelemetry.ts`). A handful of widget specs rely
 * on exactly that (ContractManager's "Awaiting contract telemetry",
 * PowerSystems's "Waiting for vessel topology…", TargetPicker/
 * DistanceToTarget's "no target" branch) — don't add those topics without
 * re-checking the spec that depends on their absence.
 *
 * `vessel.state.*` (the derived, quality-picked kinematics/body-name
 * surface most widgets actually read) only produces ANY field once ALL
 * EIGHT of its declared inputs have delivered at least one sample —
 * `vessel.orbit`, `vessel.flight`, `vessel.identity`, `system.bodies`,
 * `vessel.control`, `vessel.target`, `vessel.comms`, `vessel.propulsion`
 * (`packages/sitrep-client/src/vessel-state.ts`'s `vesselStateChannel.inputs`)
 * — so all eight are fed below, even the ones (`vessel.propulsion`,
 * `vessel.target`) no spec reads directly. `vessel.target` is fed as a
 * literal `null` payload (the mod's own "no target" wire convention,
 * mirrored by `vessel.dock`) rather than omitted — that still counts as "a
 * sample delivered" for the derived-channel gate while resolving every
 * `vessel.target.*` field read to `undefined`, matching the old fixture's
 * NO_TARGET_SENTINEL behaviour.
 */
import { createServer } from "node:http";
import { WebSocketServer } from "ws";

const PORT = Number.parseInt(process.env.SITREP_REPLAY_PORT ?? "8090", 10);

// --- Meta helpers ----------------------------------------------------------

// Quality: OnRails=0, Loaded=1. Staleness: Fresh=0, HeldStale=1, LastBeforeBlackout=2.
// PayloadMeta (inner, per-record) is a subset: { source, quality }.
//
// Quality is OnRails, not Loaded — this is the branch selector
// `deriveVesselState` (packages/sitrep-client/src/vessel-state.ts) keys off
// `vessel.orbit`'s frame `meta.quality` to pick between two mutually
// exclusive field sets: OnRails propagates the orbital elements (feeds
// apoapsisAlt/periapsisAlt/period/trueAnomaly — what SemiMajorAxis/
// CurrentOrbit/EscapeProfile/KeplerPeriod read) and nulls the
// measured-basis fields (altitudeAsl, landing*); Loaded does the reverse.
// A stable ~100km Kerbin orbit with nothing else nearby (this fixture's
// scenario) is exactly when real KSP puts a vessel on rails, so OnRails is
// also the realistic choice, not just the one that satisfies more specs.
function frameMeta(seq) {
  return {
    source: "sitrep-stream-server",
    validAt: 75469.652520361,
    seq,
    deliveredAt: 75469.652520361,
    vantage: "fixture",
    quality: 0,
    active: true,
    staleness: 0,
    timelineEpoch: 0,
  };
}

