#!/usr/bin/env node
/**
 * Single-source a package's version marker from its own package.json.
 *
 * Both `@ksp-gonogo/sitrep-sdk` (`SDK_VERSION`) and `@ksp-gonogo/sitrep-client`
 * (`CLIENT_VERSION`) used to hard-code a `"0.0.0"` literal that silently drifted
 * from the manifest (`0.0.1`). A hand-maintained version literal is a lie waiting
 * to happen — and once anything claims `apiVersion` from it, the lie ships. This
 * regenerates the exported const from `package.json` at build time so the two can
 * never disagree; the paired `version.gate.test.ts` fails CI if they ever do.
 *
 * Run from the package directory (turbo sets cwd to the package):
 *   node <repo>/scripts/gen-version.mjs SDK_VERSION
 */
import { readFileSync, writeFileSync } from "node:fs";
import { resolve } from "node:path";

const exportName = process.argv[2];
if (!exportName || !/^[A-Z][A-Z0-9_]*$/.test(exportName)) {
  console.error("usage: gen-version.mjs <EXPORT_NAME>");
  process.exit(1);
}

const pkgPath = resolve(process.cwd(), "package.json");
const { version } = JSON.parse(readFileSync(pkgPath, "utf8"));
if (typeof version !== "string" || version.length === 0) {
  console.error(`no usable "version" in ${pkgPath}`);
  process.exit(1);
}

const outPath = resolve(process.cwd(), "src/version.generated.ts");
const contents =
  `// GENERATED by scripts/gen-version.mjs from this package's package.json.\n` +
  `// Do not edit by hand — the version is single-sourced from the manifest so\n` +
  `// the exported marker can never drift from the published version. The\n` +
  `// version.gate.test.ts alongside it fails CI if this file goes stale.\n` +
  `export const ${exportName} = "${version}";\n`;

writeFileSync(outPath, contents);
console.log(`gen-version -> ${outPath} (${exportName} = ${version})`);
